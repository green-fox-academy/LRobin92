public class SumAll {
    public static void main(String[] args) {
        int total = 0;

        int [] numbers = {3, 4, 5, 6, 7};
        for (int i : numbers){
            total+=i;
        }
        System.out.println(total);
        // - Create an array variable named `numbers`
//   with the following content: `[3, 4, 5, 6, 7]`
// - Print the sum of the elements in `numbers`
    }
}
