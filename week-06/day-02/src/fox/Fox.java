package fox;

public class Fox {
  String name;
  String color;
  int age;

  public Fox() {

  }

  public Fox(String name, String color, int age) {
    this.name = name;
    this.color = color;
    this.age = age;
  }

  public String getName() {
    return name;
  }

  public String getColor() {
    return color;
  }
}
